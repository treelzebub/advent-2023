package net.treelzebub.hohoho.lastyear

import java.lang.IllegalArgumentException

enum class Result(val code: Char, val value: Int) {
    Win('Z', 6), Lose('X', 0), Draw('Y', 3);

    companion object {
        fun getByChar(char: Char) = entries.find { it.code == char }!!
    }
}

enum class Shape(val opponentId: Char, val responseId: Char, val value: Int) {
    Rock('A', 'X', 1),
    Paper('B', 'Y', 2),
    Scissors('C', 'Z', 3);

    companion object {
        fun getByChar(char: Char) = entries.find { it.opponentId == char || it.responseId == char }!!
    }

    fun score(other: Shape): Int
        = if (isWin(other)) 6
          else if (this == other) 3
          else 0

    private fun isWin(other: Shape) = beats() == other

    fun beats(): Shape = when (this) {
        Rock -> Scissors
        Paper -> Rock
        Scissors -> Paper
    }

    fun beatenBy(): Shape = when (this) {
        Rock -> Paper
        Paper -> Scissors
        Scissors -> Rock
    }
}

object Day2 {

    fun go() {
        part1()
        part2()
    }

    private fun part1() {
        val list = matrix(input).map {
            val opponent = Shape.getByChar(it.first)
            val response = Shape.getByChar(it.second)
            val reward = response.score(opponent)
            response.value + reward
        }
        val sum = list.sum()
        println("PART 1 - Final Score: $sum")
    }

    private fun part2() {
        val list = matrix(input).map {
            val opponent = Shape.getByChar(it.first)
            val desired = Result.getByChar(it.second)
            val response = when (desired) {
                Result.Win -> opponent.beatenBy()
                Result.Draw -> opponent
                Result.Lose -> opponent.beats()
            }
            response.value + desired.value
        }
        val sum = list.sum()
        println("PART 2 - Final Score: $sum")
    }

    private fun part2Scores(char: Char): Int
        = when (char) {
            'X' -> 0
            'Y' -> 3
            'Z' -> 6
            else -> throw IllegalArgumentException()
        }
}

private fun matrix(input: String): List<Pair<Char, Char>> {
    return input.split("\n").map { val i = it.split(" "); i[0][0] to i[1][0] }
}

